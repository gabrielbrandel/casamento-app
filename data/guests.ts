export function findGuest(nome: string): Guest | undefined {
  const parts = nome
    .trim()
    .toLowerCase()
    .split(/\s+/)
    .filter(Boolean)

  if (parts.length === 0) return undefined

  return guests.find((guest) => {
    const guestName = guest.nome.toLowerCase()
    return parts.every((part) => guestName.includes(part))
  })
}

